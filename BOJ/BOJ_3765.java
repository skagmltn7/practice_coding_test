package BOJ;
import java.io.*;
import java.util.*;

public class BOJ_3765 {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        String chk;
        while (true) {
            chk = br.readLine();
            if (chk == null) break;
            System.out.println(chk);
        }
    }
}

